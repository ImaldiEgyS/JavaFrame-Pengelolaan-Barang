    private void populateDataToTable() throws SQLException {
       model = (DefaultTableModel) tblBarang.getModel();
       List<Barang> brg = con.loadBarang();
                for (Barang br : brg) {
                    Object[] row = new Object[6];
                    row[0] = br.getIdBarang();
                    row[1] = br.getNama();
                    row[2] = br.getHarga();
                    row[3] = br.getTglMasuk();
                    row[4] = br.getTglKeluar();
                    row[5] = br.getStok();
                    model.addRow(row);
        }
    }
