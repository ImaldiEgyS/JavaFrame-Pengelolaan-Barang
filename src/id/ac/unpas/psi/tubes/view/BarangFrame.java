    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int status = 0;
        try {
            DefaultTableModel model = (DefaultTableModel) tblBarang.getModel();
            status = con.delete(new Barang(tfId.getText(), tfNama.getText(), Integer.parseInt(tfHarga.getText()), 
            tfMasuk.getText(), tfKeluar.getText(), Integer.parseInt(tfStok.getText())));
            refreshTable();
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(BarangFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (status == 1) {
            JOptionPane.showMessageDialog(this, "Barang Berhasil dihapus");
        } else {
            JOptionPane.showMessageDialog(this, "Barang Gagal dihapus");

        }

    }//GEN-LAST:event_btnDeleteActionPerformed

